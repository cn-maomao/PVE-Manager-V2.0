# 主函数
main() {
    # 检查是否在项目根目录
    if [[ ! -f "package.json" ]] || [[ ! -d "client" ]] || [[ ! -d "server" ]]; then
        print_error "请在PVE Manager项目根目录下运行此脚本"
        exit 1
    fi
    
    # 处理命令行参数
    case "${1:-}" in
        --help|-h)
            show_help
            exit 0
            ;;
        --init)
            init_project
            exit 0
            ;;
        --reset)
            print_warning "重置所有配置..."
            rm -f .env.global client/.env
            print_success "配置已重置"
            init_project
            exit 0
            ;;
        --reset-db)
            print_warning "重置数据库..."
            read -p "确定要删除所有数据吗？此操作不可恢复! [y/N]: " confirm_db
            if [[ $confirm_db =~ ^[Yy]$ ]]; then
                rm -f server/data/pve_manager.db
                print_success "数据库已重置，下次启动时将自动创建新数据库"
                print_info "默认管理员账户: admin / admin123"
            else
                print_info "操作已取消"
            fi
            exit 0
            ;;
        "")
            # 正常启动流程
            print_header "PVE Manager V2.0 启动脚本"
            
            # 检查系统依赖
            check_dependencies
            
            # 加载或初始化配置
            if ! load_env; then
                print_warning "未找到配置文件，开始初始化..."
                echo ""
                init_project
                echo ""
            else
                print_success "已加载配置文件"
            fi
            
            # 安装依赖
            install_dependencies
            echo ""
            
            # 构建项目
            build_project
            echo ""
            
            # 初始化数据库
            init_database
            echo ""
            
            # 创建前端环境配置（确保最新）
            create_client_env
            echo ""
            
            # 启动服务
            start_services
            ;;
        *)
            print_error "未知参数: $1"
            echo ""
            show_help
            exit 1
            ;;
    esac
}

# 运行主函数
main "$@"